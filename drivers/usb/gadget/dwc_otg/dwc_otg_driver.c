{
	return sprintf(buf, "%s\n", dwc_udc_state() ? "on" : "off");
}

static ssize_t dbg_udc_power_set(struct device_driver *drv, const char *buf,
			       size_t count)
{
	int power;
	power = simple_strtoul(buf, NULL, 16);
	if (power) {
		dwc_udc_startup();
	} else {
		dwc_udc_shutdown();
	}
	return count;
}

